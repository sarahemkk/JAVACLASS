package class2;

public class FloatingPointDemo {
    public static void main(String[] args) {
        /* by default all the floating numbers are doubles. that's why we need to write a 'f' at the end of
        the float type variables. but we don't need to worry about it as 99 of time we will be using double.
         */
        float grocery=20.5F; // is a smaller size data type
        System.out.println(grocery);
        double price=10000.555555;// is the bigger data type. can hold double the amount of float
        System.out.println(price);
    }
}
