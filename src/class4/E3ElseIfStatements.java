package class4;

public class E3ElseIfStatements {
    public static void main(String[] args) {

        int day=5;
        if(day==1){
            System.out.println("Monday");

        } else if (day==2) {
            System.out.println("Tuesday");

        } else if (day==3) {
            System.out.println("Wednesday");

        } else if (day==4){
            System.out.println("Thursday");
        } else {
            System.out.println("this day does exist");
        }


    }

}
