package class3;

public class E4Division {
    public static void main(String[] args) {
        System.out.println(10/5);
        //if the data type is int we will not see the decimal in the answer
        System.out.println(11/5);
        // if the data type will be a double/float we will see the decimal part in the answer
        System.out.println(11.0/5.0);
        int number1=10;
        int number2=4;
        System.out.println(number1/number2);
        double num1=10;
        double num2=4;
        System.out.println(num1/num2);

    }}
